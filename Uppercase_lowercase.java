//this is the ternary operator
class Uppercase_lowercase
{ 
  public static void main(String[]args)
   {
    char n='G';
    String result=(n>=65&&n<=90)?("the given value is uppercase"):("the given value is lowercase");
    System.out.println(result);
   }
}

/*output:CT:SUCESS
         RT:the given value is uppercase
*/