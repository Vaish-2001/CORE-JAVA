class Star_1_Square_Fill_Pattern
{
	public static void main(String [] sfp)
	{
		for(int i=1; i<=5; i++)
		{
			for(int j=1; j<=5; j++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}
	}
}

